//logica de programacao
//problema : receber 3 notas de um aluno,
//calcular e apresentar a media e o resultado
//se o aluno foi aprovado ou não

console.log("inicio do programa");

let nota1;
let nota2;
let nota3;

nota1 = parseInt(prompt('digite a nota 1'));
nota2 = parseInt(prompt('digite a nota 2'));
nota3 = parseInt(prompt('digite a nota 3'));


//calculo da média
media=(nota1 + nota2 + nota3) / 3;


console.log('a primeira nota do aluno é:'+ nota1);
console.log('a segunda nota do aluno é:'+ nota2);
console.log('a terceira nota do aluno é:'+ nota3);

console.log('a média do aluno é:'+ media);

console.log('fim do programa')


